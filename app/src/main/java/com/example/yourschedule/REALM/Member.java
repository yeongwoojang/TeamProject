package com.example.yourschedule.REALM;

import io.realm.RealmObject;

public class Member extends RealmObject {
    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
